package Exercicios;

public class CalculadoraSimples {
    public static void main(String[] args) {
        int n1 = 20;
        int n2 = 5;

        System.out.println("20 + 5 = " + (n1 + n2));
        System.out.println("20 - 5 = " + (n1 - n2));
        System.out.println("20 * 5 = " + (n1 * n2));
        System.out.println("20 / 5 = " + (n1 / n2));
    }
}
